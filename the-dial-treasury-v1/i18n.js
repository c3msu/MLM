  function normalizeLanguage(language) {
    return String(language || "zh").toLowerCase().startsWith("en") ? "en" : "zh";
  }

  function translate(language, key) {
    const lang = normalizeLanguage(language);
    return DICTIONARY[lang][key] || DICTIONARY.zh[key] || key;
  }

  function format(language, key, replacements) {
    return translate(language, key).replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name) => {
      if (!replacements || replacements[name] === undefined || replacements[name] === null) return match;
      return String(replacements[name]);
    });
  }

  const api = { DICTIONARY, normalizeLanguage, translate, format };
  if (typeof module !== "undefined" && module.exports) {
    module.exports = api;
  }
  root.TreasuryI18n = api;
